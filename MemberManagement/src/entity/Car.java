package entity;

public class Car {
	private int id;//车ID
	private String name;//车名称
	private int Ccount;//车的数量
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getCcount() {
		return Ccount;
	}
	public void setCcount(int ccount) {
		Ccount = ccount;
	}
}
